from odoo import api, fields, models, _
from odoo.exceptions import UserError, ValidationError


class WorkshopJobCardLine(models.Model):
    _name = "workshop.job.card.line"
    _description = "Workshop Job Card Repair Option"
    _order = "sequence, id"

    sequence = fields.Integer(default=10)
    job_card_service_id = fields.Many2one(
        "workshop.job.card.service",
        string="Job Card Service",
        required=True,
        ondelete="cascade",
        index=True,
    )
    job_card_id = fields.Many2one(
        related="job_card_service_id.job_card_id",
        store=True,
        readonly=True,
        index=True,
    )
    repair_service_id = fields.Many2one(
        related="job_card_service_id.repair_service_id",
        store=True,
        readonly=True,
    )
    repair_service = fields.Char(
        related="repair_service_id.name",
        string="Legacy Repair Service",
        store=True,
        readonly=True,
    )
    product_id = fields.Many2one(
        "product.product",
        string="Part / Option",
        required=True,
        ondelete="restrict",
        domain=[("type", "=", "consu")],
    )
    brand_id = fields.Many2one(
        related="product_id.brand_id", store=True, readonly=True
    )
    part_number = fields.Char(
        related="product_id.default_code",
        string="Part No.",
        store=True,
        readonly=True,
    )
    warranty = fields.Char()
    quantity = fields.Float(
        required=True, default=1.0, digits="Product Unit of Measure"
    )
    product_uom_id = fields.Many2one(
        "uom.uom", string="UoM", required=True, ondelete="restrict"
    )
    unit_price = fields.Monetary(required=True, default=0.0)
    amount = fields.Monetary(compute="_compute_amount", store=True, readonly=True)
    selected = fields.Boolean(string="Selected")
    generated_by_service = fields.Boolean(
        string="Generated by Repair Service",
        default=False,
        readonly=True,
        copy=False,
    )
    currency_id = fields.Many2one(
        related="job_card_id.currency_id", store=True, readonly=True
    )

    _sql_constraints = [
        (
            "workshop_job_card_line_quantity_positive",
            "check(quantity > 0)",
            "Option quantity must be greater than zero.",
        ),
        (
            "workshop_job_card_line_price_nonnegative",
            "check(unit_price >= 0)",
            "Unit price cannot be negative.",
        ),
    ]

    @api.depends("quantity", "unit_price")
    def _compute_amount(self):
        for line in self:
            line.amount = line.quantity * line.unit_price

    @api.onchange("product_id")
    def _onchange_product_id(self):
        if self.product_id:
            self.product_uom_id = self.product_id.uom_id
            self.unit_price = self.product_id.lst_price

    @api.onchange("selected", "job_card_service_id")
    def _onchange_selected(self):
        for line in self.filtered(
            lambda item: item.selected and item.job_card_service_id
        ):
            for sibling in line.job_card_service_id.option_line_ids:
                if sibling != line and sibling.selected:
                    sibling.selected = False

    @api.model_create_multi
    def create(self, vals_list):
        for vals in vals_list:
            service_line = self.env["workshop.job.card.service"].browse(
                vals.get("job_card_service_id")
            )
            card = service_line.job_card_id
            if (
                card
                and card.state != "draft"
                and not self.env.context.get("skip_job_card_state_check")
            ):
                raise UserError(_("Repair Options can only be added in Draft."))
            product = self.env["product.product"].browse(vals.get("product_id"))
            if product:
                vals.setdefault("product_uom_id", product.uom_id.id)
                vals.setdefault("unit_price", product.lst_price)
        lines = super().create(vals_list)
        lines.filtered("selected")._sync_selected_option()
        lines._validate_single_selected_option()
        return lines

    def write(self, vals):
        vals = dict(vals)
        business_fields = {
            "sequence",
            "job_card_service_id",
            "product_id",
            "warranty",
            "quantity",
            "product_uom_id",
            "unit_price",
            "selected",
        }.intersection(vals)
        for line in self:
            if line.job_card_id.state == "sent" and business_fields - {"selected"}:
                raise UserError(_("Only option selection can change after sending."))
            if line.job_card_id.state not in {"draft", "sent"} and business_fields:
                raise UserError(_("Approved or closed Repair Options cannot be modified."))
        result = super().write(vals)
        selection_changed = vals.get("selected") is True or (
            "job_card_service_id" in vals and any(self.mapped("selected"))
        )
        if selection_changed and not self.env.context.get("skip_selection_sync"):
            self.filtered("selected")._sync_selected_option()
        self._validate_single_selected_option()
        return result

    def unlink(self):
        if any(line.job_card_id.state != "draft" for line in self):
            raise UserError(_("Repair Options can only be deleted in Draft."))
        return super().unlink()

    def _sync_selected_option(self):
        """Keep the current option and clear selected siblings in its service."""
        for line in self:
            if not line.selected:
                continue
            competitors = self.search(
                [
                    ("job_card_service_id", "=", line.job_card_service_id.id),
                    ("selected", "=", True),
                    ("id", "!=", line.id),
                ]
            )
            if competitors:
                competitors.with_context(skip_selection_sync=True).write(
                    {"selected": False}
                )

    def _validate_single_selected_option(self):
        """Fallback protection for imports, RPC calls, and internal writes."""
        service_lines = self.mapped("job_card_service_id")
        for service_line in service_lines:
            selected_count = self.search_count(
                [
                    ("job_card_service_id", "=", service_line.id),
                    ("selected", "=", True),
                ],
                limit=2,
            )
            if selected_count > 1:
                raise ValidationError(
                    _(
                        "Only one Product Option may be selected for each "
                        "Job Card Repair Service."
                    )
                )

    @api.constrains("quantity", "unit_price", "product_id", "product_uom_id")
    def _check_line_values(self):
        for line in self:
            if line.quantity <= 0:
                raise ValidationError(_("Option quantity must be greater than zero."))
            if line.unit_price < 0:
                raise ValidationError(_("Unit price cannot be negative."))
            if (
                line.product_id
                and line.product_uom_id
                and line.product_uom_id.category_id != line.product_id.uom_id.category_id
            ):
                raise ValidationError(
                    _("The option UoM must use the product's UoM category.")
                )

    @api.constrains("job_card_id", "job_card_service_id")
    def _check_job_card_service(self):
        for line in self:
            if (
                line.job_card_service_id
                and line.job_card_service_id.job_card_id != line.job_card_id
            ):
                raise ValidationError(
                    _("The option line and Repair Service must belong to the same Job Card.")
                )

    @api.constrains("job_card_service_id", "product_id")
    def _check_duplicate_product_option(self):
        for line in self:
            if line.job_card_service_id and line.product_id and self.search_count(
                [
                    ("job_card_service_id", "=", line.job_card_service_id.id),
                    ("product_id", "=", line.product_id.id),
                    ("id", "!=", line.id),
                ],
                limit=1,
            ):
                raise ValidationError(
                    _(
                        "The same Product Option cannot be added more than once "
                        "to one Repair Service."
                    )
                )
